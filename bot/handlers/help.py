# "–≠—Ç–æ—Ç –±–æ—Ç –ø–æ–∑–≤–æ–ª—è–µ—Ç –≤–∞–º –ø–æ–ª—É—á–∞—Ç—å —Ç—Ä–∞–µ–∫—Ç–æ—Ä–∏—é –æ–±—É—á–µ–Ω–∏—è –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –ø–æ –∫–æ–¥—É –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è\. "
# '–ù–∞–ø—Ä–∏–º–µ—Ä, –æ—Ç–ø—Ä–∞–≤—å—Ç–µ "09\.03\.04", —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –ø—Ä–æ—Ñ–∏–ª–∏ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è "–ü—Ä–æ–≥—Ä–∞–º–º–Ω–∞—è –∏–Ω–∂–µ–Ω–µ—Ä–∏—è" '
# "–∏ –ø–æ—Å–º–æ—Ç—Ä–µ—Ç—å —É—á–µ–±–Ω—ã–π –ø–ª–∞–Ω –¥–ª—è —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–≥–æ –ø—Ä–æ—Ñ–∏–ª—è\.\n\n"
# "–í—ã —Ç–∞–∫–∂–µ –º–æ–∂–µ—Ç–µ –Ω–∞–π—Ç–∏ –ø—Ä–æ—Ñ–∏–ª–∏ –∏–ª–∏ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è –ø–æ –Ω–∞–∑–≤–∞–Ω–∏—é –∏–ª–∏ –∂–µ –≤—ã –º–æ–∂–µ—Ç–µ –∏—Å–∫–∞—Ç—å –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è "
# "–ø–æ –ø—Ä–µ–ø–æ–¥–∞–≤–∞–µ–º–æ–π –¥–∏—Å—Ü–∏–ø–ª–∏–Ω—ã\.\n\n"
# "*–ü—Ä–∏–º–µ—Ä—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è\:*\n"
# "`09.03.04`\n"
# "`–ü—Ä–æ–≥—Ä–∞–º–º–Ω–∞—è –∏–Ω–∂–µ–Ω–µ—Ä–∏—è`\n"
# "`–î–∏–∑–∞–π–Ω`",
def help_callback(update: Update, context: CallbackContext) -> None:
    update.effective_message.reply_text(
        escape_markdown(
            f"üëã –ü—Ä–∏–≤–µ—Ç, {update.message.from_user.first_name}!\n\n"
            "–≠—Ç–æ—Ç –±–æ—Ç –ø–æ–∑–≤–æ–ª—è–µ—Ç –≤–∞–º –ø–æ–ª—É—á–∞—Ç—å —Ç—Ä–∞–µ–∫—Ç–æ—Ä–∏—é –æ–±—É—á–µ–Ω–∏—è –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –ø–æ –∫–æ–¥—É –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è. "
            '–ù–∞–ø—Ä–∏–º–µ—Ä, –æ—Ç–ø—Ä–∞–≤—å—Ç–µ "09.03.04", —á—Ç–æ–±—ã –ø–æ–ª—É—á–∏—Ç—å –ø—Ä–æ—Ñ–∏–ª–∏ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏—è "–ü—Ä–æ–≥—Ä–∞–º–º–Ω–∞—è –∏–Ω–∂–µ–Ω–µ—Ä–∏—è" '
            "–∏ –ø–æ—Å–º–æ—Ç—Ä–µ—Ç—å —É—á–µ–±–Ω—ã–π –ø–ª–∞–Ω –¥–ª—è —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–≥–æ –ø—Ä–æ—Ñ–∏–ª—è.\n\n",
            version=2,
        )
        + "*–ü—Ä–∏–º–µ—Ä—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è\:*\n"
        "`09.03.04`\n",
        parse_mode=ParseMode.MARKDOWN_V2,
    )


def init_handlers(dispatcher: Dispatcher):
    # show about information
    dispatcher.add_handler(CommandHandler("help", help_callback), group=4)
    dispatcher.add_handler(CommandHandler("start", help_callback), group=4)
